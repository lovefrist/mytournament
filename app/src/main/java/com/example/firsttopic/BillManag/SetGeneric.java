package com.example.firsttopic.BillManag;

public class SetGeneric {
    private int id;
    private String carid;
    private String money;
    private String popel;
    private String time;
    public void setId(int id) {
        this.id = id;
    }

    public void setCarid(String carid) {
        this.carid = carid;
    }

    public void setMoney(String money) {
        this.money = money;
    }

    public void setPopel(String popel) {
        this.popel = popel;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public int getId() {
        return id;
    }

    public String getCarid() {
        return carid;
    }

    public String getMoney() {
        return money;
    }

    public String getPopel() {
        return popel;
    }

    public String getTime() {
        return time;
    }



}
