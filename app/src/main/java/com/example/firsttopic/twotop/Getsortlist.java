package com.example.firsttopic.twotop;

public class Getsortlist {

    private String id;
    private String red;
    private String grenn;
    private String yellow;

    public void setId(String id) {
        this.id = id;
    }

    public void setRed(String red) {
        this.red = red;
    }

    public void setGrenn(String grenn) {
        this.grenn = grenn;
    }

    public void setYellow(String yellow) {
        this.yellow = yellow;
    }

    public String getRed() {
        return red;
    }

    public String getGrenn() {
        return grenn;
    }

    public String getYellow() {
        return yellow;
    }

    public String getId() {
        return id;
    }



}
